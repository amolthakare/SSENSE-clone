let B_main=document.getElementById("B_main");
var cartArr = JSON.parse(localStorage.getItem("cartItems")) || [];
var WishlistArr = JSON.parse(localStorage.getItem("WishlistItems")) || [];

const appendData = (data,where,value) => {
    where.innerHTML=null;

    data.forEach((elem) => {
        let frame = document.createElement("div");
        frame.addEventListener("click",() => {
            item(elem);
        });

        let frame1 = document.createElement("div");

        let tit = document.createElement("p");
        tit.setAttribute("id","B_tit");
        tit.innerText=elem.tit;

        let Title=document.createElement("p");
        Title.setAttribute("id","B_Title");
        Title.innerText=elem.Title;

        let Image=document.createElement("img");
        Image.src=elem.Image;
        Image.id="B_Image";

        let Price = document.createElement("p");
        Price.setAttribute("id","B_Price");
        Price.innerText="$"+elem.Price;

        let BTN = document.createElement("div");
        BTN.setAttribute("id","B_BTN");

        let btn = document.createElement("button");
        btn.innerText="ADD TO CART";
        btn.setAttribute("id","B_btn")

        btn.addEventListener("click", function () {
            addToCart(elem);
        });

        let wishlist=document.createElement("button");
        wishlist.innerText="ADD TO WISHLIST";
        wishlist.setAttribute("id","B_wishlist");

        wishlist.addEventListener("click", function () {
            addToWishlist(elem);
        });

        BTN.append(btn,wishlist);

        frame.append(Image,tit,Title,Price);
        frame1.append(frame,BTN);

        where.append(frame1);

    });
};

